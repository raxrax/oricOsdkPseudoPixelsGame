void printLoresText(unsigned char x, unsigned char y, unsigned char type, char *text)
{
    unsigned char i;
    unsigned char l = strlen(text);

    unsigned int screenAddr = 0xbb80 + x + y * 40;

    type = type ? 16 + 32 : 32;

    for (i = 0; i < l; ++i)
    {
        unsigned char chrAddr = (text[i] - 32) * 4;

        poke(screenAddr++, (altFont[chrAddr++] + type));
        poke(screenAddr++, (altFont[chrAddr++] + type));

        poke(screenAddr + 38, (altFont[chrAddr++] + type));
        poke(screenAddr + 39, (altFont[chrAddr] + type));
    }
}